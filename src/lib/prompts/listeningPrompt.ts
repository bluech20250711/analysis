// itemNumbers에 해당하는 표 행만 골라 프롬프트에 담는다 — 유닛(문항 1개 또는 짝 그룹)
// 하나를 생성할 때 관련 없는 다른 문항의 출제 기준까지 매번 보낼 필요가 없다.
export function buildListeningSystemPrompt(options: ExamOptions, itemNumbers: number[]): string {
  const rows = PART1_ROWS.filter((entry) => entry.numbers.some((n) => itemNumbers.includes(n)));
  const table = ['| 번호 | 문항 유형 | 출제 기준 |', '|---|---|---|', ...rows.map((entry) => entry.row)].join('\n');
  const numbersLabel = itemNumbers.join(', ');

  return `${buildRoleAndPrinciples(options)}

[Part 1] 듣기 문항 출제 기준(이번에 생성할 문항만 발췌)

${table}

${LISTENING_COMMON}

이제 ${numbersLabel}번 문항을 위 JSON 스키마에 맞춰 생성하라. 정확히 ${itemNumbers.length}개의 항목을 생성해야 하며, 각 항목의 number 필드는 ${numbersLabel} 중 하나와 정확히 일치해야 한다.`;
}
