const PART1_TABLE = `
| 번호 | 문항 유형 | 출제 기준 |
|---|---|---|
| 1 | 목적 파악 | 한 화자(주로 안내방송/공지 형태)가 말하는 담화를 듣고 목적을 파악. 선택지는 "~하려고" 형태의 한국어 문장 5개. 담화는 3~5문장, 초반에 상황 제시 후 후반부에 목적이 드러나는 구조 |
| 2 | 의견 파악 | 남녀 대화(짧은 대화, 4~6턴) 후 한 화자의 의견을 파악. 선택지는 일반적 주장문 형태(예: "~해야 한다") |
| 3 | 관계 추론 | 대화 속 어휘·상황 단서로 두 화자의 직업적 관계 추론(예: 손님-점원, 의사-환자). 오답은 유사 직업군으로 구성 |
| 4 | 그림 불일치 | 하나의 그림(장면) 묘사 대화. 화자들이 그림 속 요소를 하나씩 언급하며 확인하는 구조. 마지막에 실제 그림과 다른 부분 1곳을 의도적으로 배치 |
| 5 | 할 일 파악 | 대화 후 남자 또는 여자가 대화 직후 할 일을 묻는 문제. 마지막 대사에서 반전이 있어야 함(중간까지의 흐름과 다른 결론) |
| 6 | 금액 계산 | 쇼핑/주문 상황, 품목별 가격 제시 후 할인·쿠폰 등 조건이 추가되어 최종 금액을 계산해야 정답 도출 가능하도록 구성 |
| 7 | 이유 파악 | 제안을 거절하거나 못하는 이유를 대화 속에서 파악. 초반 제안 → 중반 다른 이유로 오해 유도 → 후반 실제 이유 제시 |
| 8 | 언급되지 않은 것 | 특정 주제(행사, 강좌, 여행지 등)에 대해 화자가 4가지 항목을 언급, 선택지 5개 중 언급되지 않은 1개가 정답 |
| 9 | 내용 일치/불일치 | 한 화자의 안내 담화(행사 소개 등)를 듣고 5개 선택지(영어 문장) 중 내용과 다른 것 고르기 |
| 10 | 표(도표) 문제 | 표 제시 → 대화에서 2~3가지 조건(가격, 기능, 날짜 등) 언급 → 조건을 만족하는 항목 고르기 |
| 11 | 짧은 대화 응답 | 대화 마지막 화자의 말에 대한 가장 적절한 응답 선택. 대화는 2~3턴으로 짧게 |
| 12 | 짧은 대화 응답 | 11번과 유형 동일, 다른 소재(일상 대화) |
| 13 | 긴 대화 응답 | 4~6턴의 비교적 긴 대화 후 마지막 응답 선택. 논리적 흐름 반전 요소 포함 |
| 14 | 긴 대화 응답 | 13번과 동일 유형, 다른 소재 |
| 15 | 상황에 적절한 말 | 한 사람의 서술(내레이션) 형태로 상황 설명 → 마지막에 "이런 상황에서 A가 B에게 할 말로 가장 적절한 것은?" 질문. 서술은 5~7문장, 상황 맥락이 분명해야 함 |
| 16-17 | 긴 담화(공통 지문 2문항) | 하나의 긴 담화(1인 발화, 강의/설명문 형식, 8~10문장)를 듣고 두 문제 풀이. 16번=주제/목적 파악, 17번=언급되지 않은 것(또는 세부내용 불일치). 담화 내에 5개 이상의 세부 항목이 나열되는 구조 필요 |
`.trim();

const LISTENING_COMMON = `
듣기 공통 요구사항
- type 필드에는 출제기준표의 유형명(예: "목적 파악")을, instruction 필드에는 실제 시험지에 인쇄되는 지시문 전체 문장(예: "다음을 듣고, 남자가 하는 말의 목적으로 가장 적절한 것을 고르시오.")을 각각 채울 것 — 화자 성별(남자/여자)은 speakers 배열과 일치해야 함
- 각 문항마다 영어 대본(스크립트) 전체를 먼저 제시하고, 화자(M/W/Narrator)별 대사 단위로 분리해서 제시
- script의 각 대사 줄마다 자연스러운 한국어 해석을 scriptKo 배열에 같은 순서로 대응시켜 생성 (scriptKo의 길이는 script 길이와 반드시 동일해야 함). 실제 시험지에서는 이 한국어 해석이 대본 각주에 함께 실림
- 대본 아래에 선택지(1~5) 제시
- 정답 번호와 정답 판단 근거 문장을 스크립트에서 직접 인용해 explanation에 포함
- 발음 유사 어휘 함정(예: thirteen/thirty), 숫자 계산 함정 등 실제 수능 스타일 함정 요소 최소 1개 이상 포함
- 16-17번은 같은 script/passage를 공유하므로 pairGroupId를 "16-17"로 동일하게 지정
- 4번(그림 불일치), 10번(표 문제)은 imageRef 필드에 이미지 설명을 간단히 문자열로 남길 것 (예: "그림: 공원 벤치, 나무, 강아지")
`.trim();

export function buildListeningSystemPrompt(options: ExamOptions): string {
  return `${buildRoleAndPrinciples(options)}

[Part 1] 듣기 1~17번 문항별 출제 기준

${PART1_TABLE}

${LISTENING_COMMON}

이제 1번부터 17번까지 총 17개의 듣기 문항을 위 JSON 스키마에 맞춰 생성하라.`;
}
