export type ReadingRange = '18-34' | '35-45';

export function buildReadingSystemPrompt(
  options: ExamOptions,
  range: ReadingRange,
  usedTopics: string[] = [],
): string {
  const table = range === '18-34' ? PART2_TABLE_18_34 : PART2_TABLE_35_45;
  const numbersLabel = range === '18-34' ? '18번부터 34번까지 (총 17문항)' : '35번부터 45번까지 (총 11문항)';

  return `${buildRoleAndPrinciples(options)}

[Part 2] 독해 18~45번 문항별 출제 기준

${table}

${READING_COMMON}${buildAvoidDuplicationNote(usedTopics)}

이제 ${numbersLabel}의 독해 문항을 위 JSON 스키마에 맞춰 생성하라.`;
}
